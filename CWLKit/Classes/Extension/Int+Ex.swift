import Foundation

public extension Int {
    var uIntValue: UInt {
        return UInt(self)
    }
    
    var uInt8Value: UInt8 {
        return UInt8(self)
    }
    
    var uInt16Value: UInt16 {
        return UInt16(self)
    }
    
    var uInt32Value: UInt32 {
        return UInt32(self)
    }
    
    var uInt64Value: UInt64 {
        return UInt64(self)
    }
    
    var doubleValue: Double {
        return Double(self)
    }
    
    var floatValue: Float {
        return Float(self)
    }
    
    var cgFloatValue: CGFloat {
        return CGFloat(self)
    }
}
